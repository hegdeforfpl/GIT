#Comparision
#==   equal
num1 = 2
num2 = 3
print(num1 == num2)
#!=   not equal
print(num1 != num2)
#>   greater than    >= greater than or equal
print(num1 > num2)
print(num1 >= num2)
#<   less than    <= less than or equal
print(num1 < num2)
print(num1 <= num2)

#String to int
num11 = "100"
num22 = "200"

print(int(num11) + int(num22))

#int to Str
num11 = 100
num22 = 200

print(str(num11) + str(num22))

